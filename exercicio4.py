"""Faça um Programa que verifique se uma letra digitada é vogal ou consoante."""

letra = str(input('Digite uma letra: ')).upper()

if __name__ == '__main__':
    if letra == 'A':
        print('A letra é uma vogal')
    elif letra == 'E':
        print('A letra é uma vogal')
    elif letra == 'I':
        print('A letra é uma vogal')
    elif letra == 'O':
        print('A letra é uma vogal')
    elif letra == 'U':
        print('A letra é uma vogal')
    else:
        print('A letra é uma consoante')